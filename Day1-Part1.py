#Basic arithmetic
a = 8
b = 2
print('Assigned Values:\t\t', 'a =', a, '\tb =', b)
print('Addition:\t', a, '+', b, '=', a + b )
print('Subtraction:\t', a, '-', b, '=', a - b)
print('Multiplation:\t', a, 'x', b, '=', a - b)
print('Division:\t', a, '/', b, '=', a/b)
print('Floor Devision:\t', a, '%', b, '=', a//b)
print('Modulo:\t', a, '~', b, '=', a % b)

print('Exponent:\t', a, '^2 =', a**b, sep ='')

